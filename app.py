temperature = int(input("Enter the temperature: "))
print("Temperature is:", temperature)
if temperature > 30:
    print("It's hot")
    print("Stay hydrated")
elif temperature < 20:
    print("It's cold")
    print("Wear a jacket")
elif temperature == 30:
    print("It's a warm day")
    print("Perfect for a walk")
print("Have a great day!")
